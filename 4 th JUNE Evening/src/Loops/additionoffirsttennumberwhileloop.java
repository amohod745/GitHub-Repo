package Loops;

public class additionoffirsttennumberwhileloop {

	public static void main(String[] args) {
     // WAP addition first 10 numbers using while loop
	
		int sum=0;
		int i=1;

		
		while (i<=10){
			
			sum = sum+i;
			i++;
				
		}
		System.out.println(sum);

		System.out.println("hello");

	}

}


