package operators;

public class IncDecOperator {

	public static void main (String[]args) {
		
		int A=10;
		int B=20;
		
		int C=A;
		
		System.out.println(C);
		
		C=B;
		
	    System.out.println(C);
	    
	    C=1002;
	    
	    System.out.println(C);
	    
	    C++;   // increment by one 
	   
	    System.out.println(C);
	    
	    C=C+1; // increment by one
	    
	    System.out.println(C);
	    
	    C--;  // decrement by one 
	    
	    System.out.println(C);
		
		C=C-1;// Decrement by one 
		
		System.out.println(C);
	}
}
