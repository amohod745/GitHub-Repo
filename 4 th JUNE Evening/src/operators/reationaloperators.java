package operators;

public class reationaloperators {
	
	public static void main(String [] args) {
		
		// Reational operators //
		  
		
	//   a   b      a<b      a>b     a>=b      a<=b      a==b    a!=b  
		
	// 	10  20     TRUE     FLASE    TRUE     FLASE     FLASE    TRUE
		
		int a=10;
		int b=20;
		
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);

	
	}

}
