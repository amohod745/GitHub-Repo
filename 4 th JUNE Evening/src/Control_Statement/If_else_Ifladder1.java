package Control_Statement;

public class If_else_Ifladder1 {

	public static void main(String[] args) {
         
		int Marks =75;
		 if(Marks>=65) {
			 System.out.println("Result : Districation");
		 }
		 
		 else if(Marks>=60) {
			 System.out.println("Result : 1 st Class");
		 }
		 else if(Marks>=55) {
			 System.out.println("Result : Higher 2 nd Class");
		 }
		 else if(Marks>=50) {
			 System.out.println("Result : 2 nd Class");
		 }
		 else if(Marks>=35){
			 System.out.println("Result : Pass");
		 }
		 else {
			 System.out.println("Result : Fail");
		 }
		 
	}

}
