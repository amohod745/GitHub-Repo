package Interface;

public class final1 {

	public static void main(String[] args) {
         int i =45 ;
         System.out.println(i);
         
         i=10;
         System.out.println(i);
         
         i=25;
         System.out.println(i);
         
         final int j=125;
         System.out.println(j);
         
         //  j=254; j declared as final 
	}

}
